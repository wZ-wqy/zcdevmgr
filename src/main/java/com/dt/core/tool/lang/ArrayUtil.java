    /**
     * 把多个数组按顺序合并成一个数组
     */
    public static Object[] merege(Object[]... arrs) {
        int total = 0;
        for (Object[] arr : arrs) {
            total += arr.length;
        }
        Object[] newArr = new Object[total];

        int i = 0;
        for (Object[] arr : arrs) {
            System.arraycopy(arr, 0, newArr, i, arr.length);
            i += arr.length;
        }
        return newArr;
    }

